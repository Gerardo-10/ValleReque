class Empleado:
    def __init__(self, id_empleado, nombre_empleado, apellido_empleado, dni, direccion, telefono, correo_electronico, fecha_nacimiento):
        self.id_empleado = id_empleado
        self.nombre = nombre_empleado
        self.apellido = apellido_empleado
        self.dni = dni
        self.direccion = direccion
        self.correo = correo_electronico
        self.telefono = telefono
        self.fecha_nacimiento = fecha_nacimiento
