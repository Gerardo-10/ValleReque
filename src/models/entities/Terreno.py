class Terreno:
    def __init__(self, id_terreno, id_proyecto, etapa, unidad, area, precio_terreno, estado_terreno, tipo_terreno,
                 manzana, numero_lote, codigo_unidad):
        self.id_terreno = id_terreno
        self.id_proyecto = id_proyecto
        self.etapa = etapa
        self.unidad = unidad
        self.area = area
        self.precio = precio_terreno
        self.estado = estado_terreno
        self.tipo = tipo_terreno
        self.manzana = manzana
        self.lote = numero_lote
        self.codigo_unidad = codigo_unidad
